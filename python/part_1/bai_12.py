import math

x = float(input("x: "))

# Trị tuyệt đối
print("|x| = ", math.fabs(x))
# Căn bậc hai
print("sqrt(x) = ", math.sqrt(x))
# Làm tròn lên
print("x_up = ", math.ceil(x))
# Làm tròn xuống
print("x_down = ", math.floor(x))
# Số pi
print("pi = ", math.pi)